import java.util.Scanner;
class InputMonthNumberIf{
public static void main(String args[])
{
Scanner scob=new Scanner(System.in);
int Mn;
Mn=scob.nextInt();
if(Mn==1){System.out.println("JANUARY");}
else if(Mn==2){System.out.println("FEBRUARY");}
else if(Mn==3){System.out.println("MARCH");}
else if(Mn==4){System.out.println("APRIL");}
else if(Mn==5){System.out.println("MAY");}
else if(Mn==6){System.out.println("JUNE");}
else if(Mn==7){System.out.println("JULY");}
else if(Mn==8){System.out.println("AUGUST");}
else if(Mn==9){System.out.println("SEPTEMBER");}
else if(Mn==10){System.out.println("OCTOBER");}
else if(Mn==11){System.out.println("NOVEMBER");}
else if(Mn==12){System.out.println("DECEMBER");}
else {System.out.println("INVALID");}
}}
